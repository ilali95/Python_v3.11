# ✔ Функция получает на вход словарь с названием компании в качестве ключа
# и списком с доходами и расходами (3-10 чисел) в качестве значения.
# ✔ Вычислите итоговую прибыль или убыток каждой компании. Если все компании
# прибыльные, верните истину, а если хотя бы одна убыточная — ложь.

def calculate_profit(dictionary):
    for company, values in dictionary.items():
        total_income = sum(values[:3])  # Сумма первых трех элементов списка - доходы
        total_expenses = sum(values[3:])  # Сумма остальных элементов списка - расходы
        profit = total_income - total_expenses
        if profit < 0:
            return False  # Если есть убыток, возвращаем False
    return True  # Если все компании прибыльные, возвращаем True

companies = {
    'Company A': [100, 50, 200, 80, 30, 50],  # Доходы: 100 + 50 + 200 = 350, Расходы: 80 + 30 + 50 = 160, Прибыль: 350 - 160 = 190
    'Company B': [200, 150, 100, 120, 80, 90],  # Доходы: 200 + 150 + 100 = 450, Расходы: 120 + 80 + 90 = 290, Прибыль: 450 - 290 = 160
    'Company C': [50, 80, 120, 40, 60, 70]  # Доходы: 50 + 80 + 120 = 250, Расходы: 40 + 60 + 70 = 170, Прибыль: 250 - 170 = 80
}

result = calculate_profit(companies)
print(result)  # Вывод: True, так как все компании прибыльные
